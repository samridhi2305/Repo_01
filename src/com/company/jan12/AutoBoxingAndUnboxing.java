package com.company.jan12;

public class AutoBoxingAndUnboxing {
    public static void main(String[] args) {


        int i = 10;
        Integer value = new Integer(i);//Autoboxing-Converting primitive type to its Wrapper class.

        Integer j = new Integer(50);
        int k = j;//Auto unboxing - vice versa, Converting Wrapper class type to its primitive type.

    }
}

